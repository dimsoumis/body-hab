	<p><strong>Before You Start (Camera Setup)</strong></p>

	<p>To ensure accurate motion tracking and feedback:</p>

<ul><li>Stand upright facing the camera.</li>
<li>Position yourself so your full body is visible from head to feet.</li>
<li>Stand with your feet hip-width apart.</li>
<li>Keep your arms relaxed by your sides.</li>
<li>Look straight ahead at the screen.</li>
<li>Make sure the lighting is good and there is enough space around you.</li>
<li>Remove objects near your feet that could affect balance.</li>
	</ul>




<p><strong>How to Perform the Exercise</strong></p>

<ul><li>Shift your weight onto one leg.</li>
<li>Slowly lift the opposite foot off the ground.</li>
<li>Bend the lifted knee slightly so the foot is not touching the floor.</li>
<li>Keep your standing leg straight but not locked.</li>
<li>Maintain an upright posture:
<ul><li>Chest up</li>
<li>Back straight</li>
<li>Hips level</li>
<li>Eyes forward</li></ul></li>
<li>Hold this position as still as possible (or until instructed to stop).</li></ul>
	
<p>If you lose balance, simply reset and try again.</p>
	
<p><strong>What the System Is Measuring</strong></p>

<p>The platform uses body landmarks to monitor:</p>

<ul><li>Hip stability</li>
<li>Knee alignment</li>
<li>Ankle control</li>
<li>Body sway</li>
<li>Duration of balance hold</li></ul>
	
<p><strong>Good Form Checklist</strong></p>
	
<ul><li>Standing foot flat on the floor</li>
<li>Hips level (not dropping to one side)</li>
<li>Shoulders relaxed</li>
<li>Minimal upper-body swaying</li>
<li>Eyes looking forward</li>
	</ul>

	<p><u>Tips</u></p>


<p>✔ Focus on a fixed point at eye level.<br>
✔ Engage your core muscles.<br>
✔ Breathe slowly and steadily.<br>
✔ Keep movements controlled when lifting and lowering your foot.</p>

<p>Avoid:</p>

<p>❌ Leaning excessively to one side<br>
❌ Looking down at your feet<br>
❌ Locking the standing knee<br>
❌ Holding your breath<br>
❌ Letting the lifted foot touch the ground repeatedly</p>

<p>Try to minimize movement and avoid touching the lifted foot to the ground.</p>
	
	
	

<p><strong>Safety Guidelines</strong></p>

<ul><li>If you feel unsteady, stand near a wall or stable object for safety.</li>
<li>Stop immediately if you feel pain or dizziness.</li>
<li>Perform the exercise barefoot or with stable athletic shoes.</li>
	</ul>




	
		

	
