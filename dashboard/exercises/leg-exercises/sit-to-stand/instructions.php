	<h1 id="pageTitle">Sit to Stand</h1>
	
<p style="text-align: center; margin-bottom: 70px;">Read instructions on how to perform the exercise.</p>
	
	
<p><img id="sitToStandInstruImage" style="float: right;
  width: 272px;
  margin: 0px 0px 15px 30px;" src="/images/sit-to-stand-instru.gif">The Sit-to-Stand Test measures lower body strength, balance, and functional mobility.<br>
If you have questions on how to perform the exercise correctly, follow the tips below.</p>

	<p><strong>Before You Start (Camera Setup)</strong></p>

	<p>To ensure accurate motion tracking and feedback:</p>

<ul><li>Place your webcam at about hip height.</li>
<li>Position the camera directly in front of you.</li>
<li>Stand 2–3 meters away from the camera.</li>
<li>Make sure your full body and the chair are visible.</li>
<li>Ensure good lighting from the front.</li>
<li>Avoid cluttered backgrounds.</li>
<li>Use a stable chair (no wheels).</li>
<li>When ready, start the exercise.</li>
	</ul>
	
<p><strong>Chair Setup</strong></p>

<ul><li>Use a standard chair (approximately knee height).</li>
<li>Place it against a wall for stability if needed.</li>
<li>The chair should not move during the test.</li>
<li>Sit in the middle of the seat (not too close to the edge).</li></ul>

<p><strong>Starting Position</strong></p>

<ul><li>Sit upright with your back straight.</li>
<li>Feet flat on the floor, shoulder-width apart.</li>
<li>Knees bent at approximately 90 degrees.</li>
<li>Arms crossed over your chest (recommended for standard testing).</li>
</ul>

<p><strong>Movement Execution</strong></p>

<ul><li>Lean slightly forward from the hips.</li>

<li>Stand up fully until:

<ul><li>Hips are fully extended.</li>

<li>Knees are straight.</li>

<li>Torso is upright.</li></ul></li>

<li>Pause briefly in the standing position.</li>

<li>Slowly lower yourself back down to a fully seated position.</li>

<li>Make sure your hips clearly contact the chair before starting the next repetition.</li></ul>


	
	
<p>The system counts one repetition when you reach full standing position and you return successfully to a complete seated position.</p>
	
<p><strong>Test Format</strong></p>

The test can be performed in 4 different formats:

<ul><li>30-second test (maximum repetitions in 30 seconds),</li>
<li>60-second test (maximum repetitions in 60 seconds),</li>
	<li>5 Times Sit-to-Stand Test (5xSTS) (time to complete 5 repetitions),</li>
<li>10 Times Sit-to-Stand Test (10xSTS) (time to complete 10 repetitions).</li></ul>

	<p><u>Tips</u></p>

<p>✔ Keep feet flat and stable.<br>
✔ Push through your heels when standing.<br>
✔ Keep knees aligned over toes.<br>
✔ Maintain control while sitting down.<br>
✔ Keep your torso upright at full standing.</p>

<p>Avoid:</p>

<p>❌ Using momentum.<br>
❌ Rocking excessively.<br>
❌ Letting knees collapse inward.<br>
❌ Partial standing (hips not fully extended).<br>
❌ “Touch-and-go” sitting without full contact.</p>

<p>Move with control and full range of motion.<br>
Accurate, complete repetitions improve both your results and tracking precision.</p>
	
<p><strong>About System's Feedback</strong></p>

<p>The system will:</p>
	
	<ul><li>Track hip and knee angles.</li>
<li>Detect full extension.</li>
<li>Count valid repetitions.</li>
<li>Monitor speed and symmetry.</li>
<li>Alert you if:
<ul><li>You do not fully stand.</li>
<li>You do not fully sit.</li>
<li>Your body moves out of camera view.</li></ul></li></ul>

<p>If tracking stops:</p>

<ul><li>Step slightly back.</li>
<li>Ensure your entire body remains visible.</li>
<li>Improve lighting conditions.</li></ul>

<p><strong>Safety Guidelines</strong></p>

<ul><li>Use a stable, non-moving chair.</li>
<li>Perform the test in a safe area free of obstacles.</li>
<li>Stop immediately if you feel dizziness or pain.</li>
<li>If you have recent knee, hip, or balance issues, consult a healthcare professional before performing the test.</li>
	</ul>




	
		

	
