  margin: 0px 0px 15px 30px;" src="/images/bicep-curls-instru.gif">Bicep Curls strengthen the front of your upper arms (biceps) and improve arm control and endurance.<br>
	If you have questions on how to perform the exercise correctly, follow the tips below.</p>

	<p><strong>Before You Start (Camera Setup)</strong></p>

	<p>To ensure accurate motion tracking and feedback:</p>

<ul><li>Place your webcam at chest height (if possible).</li>
<li>Stand about 1.5–2 meters away from the camera.</li>
<li>Make sure your full upper body (head to hips) is clearly visible.</li>
<li>Ensure good lighting (avoid strong backlight behind you).</li>
<li>Wear clothing that contrasts with your background.</li>
<li>When ready, start the exercise.</li>
	</ul>

<p><strong>Starting Position</strong></p>

<ul><li>Stand upright with your feet shoulder-width apart.</li>
<li>Keep your back straight and chest lifted.</li>
<li>Hold a dumbbell (or similar object) in each hand.</li>
<li>Let your arms hang naturally at your sides.</li>
<li>Keep your elbows close to your torso.</li>
<li>Palms should face forward.</li></ul>

<p><strong>Movement Execution</strong></p>

<ul><li>Slowly bend your elbows to lift the weights upward.</li>
<li>Keep your elbows stationary and close to your body.</li>
<li>Raise the weights until your forearms are vertical.</li>
<li>Briefly pause at the top.</li>
<li>Slowly lower the weights back to the starting position.</li>
<li>Fully extend your arms without locking the elbows.
	</li></ul>
	
<p>The platform will count one repetition after your elbow angle closes fully during the lift, and your arms return to the extended position.</p>
	


	<p><u>Tips</u></p>

<p>✔ Move in a slow and controlled manner.<br>
✔ Keep your shoulders relaxed.<br>
✔ Keep your torso still — avoid swinging.<br>
✔ Breathe out while lifting, breathe in while lowering.</p>

<p>❌ Do not use momentum.<br>
❌ Do not lift your elbows forward.</p>

<p>Focus on proper form rather than speed.<br>
	Correct technique ensures accurate tracking, better results, and reduced risk of injury.</p>
	
<p><strong>About System's Feedback</strong></p>

<p>The system will:</p>
	
	<ul><li>Indicate the time or repetitions you need to perform.</li>
<li>Count your repetitions automatically.</li>
<li>Monitor your elbow angle.</li>
<li>Detect incomplete reps.</li>
<li>Alert you if your posture changes significantly.</li></ul>

<p>If tracking stops:</p>

<ul><li>Check your position in the camera frame.</li>
<li>Adjust lighting if necessary.</li>
<li>Return to starting position.</li></ul>

<p><strong>Safety Guidelines</strong></p>

<ul><li>Use a weight that allows controlled movement.</li>
<li>Stop immediately if you feel pain (not normal muscle fatigue).</li>
<li>Keep movements smooth and controlled.</li>
<li>If you have shoulder or elbow issues, consult a physician before training.</li>
	</ul>




	
		

	
