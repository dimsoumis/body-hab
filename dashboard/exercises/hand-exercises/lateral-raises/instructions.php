<p><img id="lateralRaisesInstruImage" style="float: right;
  width: 272px;
  margin: 0px 0px 15px 30px;" src="/images/lateral-raises-instru.gif">The Lateral Raises exercise strengthens your shoulder muscles (especially the middle deltoids) and improves shoulder stability and control.<br>
Follow the instructions below to perform the exercise correctly and ensure accurate tracking by the platform.</p>

	<p><strong>Before You Start (Camera Setup)</strong></p>

	<p>To ensure accurate motion tracking and feedback:</p>

<ul><li>Place your webcam at chest height (if possible).</li>
<li>Stand about 1.5–2 meters away from the camera.</li>
<li>Make sure your full upper body (head to hips) is clearly visible.</li>
<li>Ensure good lighting (avoid strong backlight behind you).</li>
<li>Wear clothing that contrasts with your background.</li>
<li>When ready, start the exercise.</li>
	</ul>

<p><strong>Starting Position</strong></p>

<ul><li>Stand upright with feet shoulder-width apart.</li>
<li>Hold a dumbbell in each hand (optional).</li>
<li>Arms relaxed at your sides.</li>
<li>Palms facing inward (neutral grip).</li>
<li>Keep your chest lifted and shoulders relaxed.</li>
<li>Slight bend in your elbows (do not lock them).</li>
<li>Engage your core and keep your back neutral.</li></ul>

<p><strong>Movement Execution</strong></p>

<ul><li>Raise both arms outward to the sides.</li>
<li>Keep a slight bend in your elbows.</li>
<li>Lift until your arms reach approximately shoulder height (parallel to the floor).</li>
<li>Keep your wrists neutral and aligned with your elbows.</li>
<li>Pause briefly at the top.</li>
<li>Slowly lower your arms back to the starting position.</li>
	</ul>
	
<p>The platform will count one repetition after your arms reach shoulder height and you return to the fully lowered starting position.</p>
	
	<p><u>Tips</u></p>

<p>✔ Lift in a controlled and steady motion.<br>
✔ Keep your torso upright (do not lean forward or backward).<br>
✔ Keep shoulders down — avoid shrugging.<br>
✔ Breathe out while lifting.<br>
✔ Breathe in while lowering.</p>

<p>❌ Do not swing your body.<br>
❌ Do not use momentum.<br>
❌ Do not lift above shoulder height.</p>
	
	<p>Focus on controlled shoulder movement and proper posture rather than speed.<br>
Correct form improves tracking accuracy, enhances shoulder strength, and reduces injury risk.</p>
	
<p><strong>About System's Feedback</strong></p>

<p>The system will:</p>
	
	<ul><li>Automatically count repetitions.</li>
<li>Monitor shoulder and elbow angles.</li>
<li>Detect incomplete raises.</li>

<li>Alert you if:<ul>
<li>Your arms are not lifted high enough.</li>
<li>You are leaning excessively.</li>
<li>Your movement is too fast.</li></ul></li></ul>

<p>If tracking stops:</p>

<ul><li>Ensure both arms remain fully visible.</li>
<li>Avoid moving too close to the camera.</li>
<li>Improve lighting if needed.</li></ul>

<p><strong>Safety Guidelines</strong></p>

<ul><li>Use light to moderate weights.</li>
<li>Stop if you feel sharp shoulder pain.</li>
<li>Keep movements controlled and smooth.</li>
<li>If you have shoulder injuries, consult a healthcare professional before performing this exercise.</li>
	</ul>




	
		

	
