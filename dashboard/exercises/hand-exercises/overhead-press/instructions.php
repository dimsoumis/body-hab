<p><img id="overheadPressInstruImage" style="float: right;
  width: 272px;
  margin: 0px 0px 15px 30px;" src="/images/overhead-press-instru.gif">The Overhead Press strengthens your shoulders, upper arms, and core stability.<br>
Follow the instructions below to perform the exercise correctly and ensure accurate tracking by the platform.</p>

	<p><strong>Before You Start (Camera Setup)</strong></p>

	<p>To ensure accurate motion tracking and feedback:</p>

<ul><li>Place your webcam at chest height (if possible).</li>
<li>Stand about 1.5–2 meters away from the camera.</li>
<li>Make sure your full upper body (head to hips) is clearly visible.</li>
<li>Ensure good lighting (avoid strong backlight behind you).</li>
<li>Wear clothing that contrasts with your background.</li>
<li>When ready, start the exercise.</li>
	</ul>

<p><strong>Starting Position</strong></p>

<ul><li>Stand upright with feet shoulder-width apart.</li>
<li>Hold a dumbbell in each hand (optional).</li>
<li>Raise the dumbbells to shoulder height.</li>
<li>Elbows bent at approximately 90°.</li>
<li>Palms facing forward.</li>
<li>Keep your chest lifted and core engaged.</li>
<li>Wrists stacked above elbows.</li></ul>

<p><strong>Movement Execution</strong></p>

<ul><li>Press the dumbbells upward by extending your elbows.</li>
<li>Move the weights vertically above your shoulders.</li>
<li>At the top position, arms should be almost fully extended (not locked).</li>
<li>Keep your head neutral and avoid arching your back.</li>
<li>Slowly lower the dumbbells back to shoulder height.</li>
<li>Return to the starting 90° elbow position.</li>
	</ul>
	
<p>The platform will count one repetition after your arms reach near full extension overhead and you return to the controlled shoulder-height position.</p>
	


	<p><u>Tips</u></p>

<p>✔ Keep your core tight to avoid lower back arching.<br>
✔ Keep your elbows slightly in front of your shoulders (not flared excessively).<br>
✔ Move in a slow, controlled manner.<br>
✔ Breathe out while pressing up.<br>
✔ Breathe in while lowering down.</p>

<p>❌ Do not lean backward.<br>
❌ Do not rush the movement.</p>
	
	<p>Focus on maintaining correct alignment and controlled movement rather than speed.<br>
		Proper form ensures better tracking accuracy, improved results, and reduced injury risk.</p>
	
<p><strong>About System's Feedback</strong></p>

<p>The system will:</p>
	
	<ul><li>Automatically count repetitions.</li>
<li>Track elbow and shoulder angles.</li>
<li>Detect incomplete presses.</li>
<li>Alert you if your posture deviates significantly (e.g., excessive trunk lean).</li></ul>

<p>If tracking stops:</p>

<ul><li>Check your position in the camera frame.</li>
<li>Adjust lighting if necessary.</li>
<li>Return to starting position.</li></ul>

<p><strong>Safety Guidelines</strong></p>

<ul><li>Use a weight that allows controlled movement.</li>
<li>Stop immediately if you feel pain (not normal muscle fatigue).</li>
<li>Keep movements smooth and controlled.</li>
<li>If you have shoulder or elbow issues, consult a physician before training.</li>
	</ul>




	
		

	
